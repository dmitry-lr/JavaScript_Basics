// Реализовать четыре основные арифметические операции в виде функций с двумя параметрами.
// Обязательно использовать оператор return.

function simpleSum(num1, num2) {
    return num1 + num2;
}

function simpleSubstraction(num1, num2) {
    return num1 - num2;
}

function simpleMultiplication(num1, num2) {
    return num1 * num2;
}

function simpleDivizion(num1, num2) {
    return num1 / num2;
}

console.log(simpleSum(24, 4));
console.log(simpleSubstraction(24, 4));
console.log(simpleMultiplication(24, 4));
console.log(simpleDivizion(24, 4));
